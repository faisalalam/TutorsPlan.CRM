const initialValues = {
  title: '',

  tags: '',

  prompt: '',

  save_as_library: false,
};

const Master_ai_prompt_librariesNew = () => {
  const router = useRouter();
  const dispatch = useAppDispatch();

  const handleSubmit = async (data) => {
    await dispatch(create(data));
    await router.push(
      '/master_ai_prompt_libraries/master_ai_prompt_libraries-list',
    );
  };
  return (
    <>
      <Head>
        <title>{getPageTitle('New Item')}</title>
      </Head>
      <SectionMain>
        <SectionTitleLineWithButton
          icon={mdiChartTimelineVariant}
          title='New Item'
          main
        >
          {''}
        </SectionTitleLineWithButton>
        <CardBox>
          <Formik
            initialValues={initialValues}
            onSubmit={(values) => handleSubmit(values)}
          >
            <Form>
              <FormField label='Title'>
                <Field name='title' placeholder='Title' />
              </FormField>

              <FormField label='Tags' hasTextareaHeight>
                <Field name='tags' as='textarea' placeholder='Tags' />
              </FormField>

              <FormField label='Prompt' hasTextareaHeight>
                <Field name='prompt' as='textarea' placeholder='Prompt' />
              </FormField>

              <FormField label='SaveasLibrary' labelFor='save_as_library'>
                <Field
                  name='save_as_library'
                  id='save_as_library'
                  component={SwitchField}
                ></Field>
              </FormField>

              <BaseDivider />
              <BaseButtons>
                <BaseButton type='submit' color='info' label='Submit' />
                <BaseButton type='reset' color='info' outline label='Reset' />
                <BaseButton
                  type='reset'
                  color='danger'
                  outline
                  label='Cancel'
                  onClick={() =>
                    router.push(
                      '/master_ai_prompt_libraries/master_ai_prompt_libraries-list',
                    )
                  }
                />
              </BaseButtons>
            </Form>
          </Formik>
        </CardBox>
      </SectionMain>
    </>
  );
};

Master_ai_prompt_librariesNew.getLayout = function getLayout(
  page: ReactElement,
) {
  return (
    <LayoutAuthenticated permission={'CREATE_MASTER_AI_PROMPT_LIBRARIES'}>
      {page}
    </LayoutAuthenticated>
  );
};

export default Master_ai_prompt_librariesNew;
